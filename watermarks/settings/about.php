<h2>About</h2>

<p>
    Our thesis project presents a watermarking system designed to help protect original creators from image theft. By allowing users to easily add customized watermarks—whether text-based or image-based—this system aims to safeguard artworks from unauthorized use and distribution.
</p>

<p>
    The watermarks can be adjusted to match the colors and style of the artwork, ensuring both protection and visual harmony. In a digital age where creative works are often vulnerable to misuse, our project highlights an effective method for creators to assert ownership and maintain control over their original content.
</p>

